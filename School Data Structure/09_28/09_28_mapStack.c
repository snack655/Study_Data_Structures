int Si, Sj, Ei, Ej;
int success, top, Path_i[100], Path_j[100];
// success : 미로탐색 성공여부
// top : 시스템 스택에 저장되는 위치
// Path_i, Path_j 미로 탐색한 경로 저장

int visit(int, int);

int main() {
    top = 0;
    success = 0;    // 미로 탐색을 하지 않음! or 실패!
    printf("=== 미로 탈출 경로 찾기 게임! ===\n");
    Si = 1; Sj = 1;
    Ei = 28, Ej = 28;
    if(visit(Si, Sj) == 0)
        printf("미로의 출구를 찾을 수 없음..\n");
    else
        printf("미로의 모든 경로를 찾음!!\n");
    return 0;
}

void printPath() {
    for(int i = 0; i < top; i++)
        printf("[%d, %d]", Path_i[i], Path_j[i]);
    printf("\n");
}

int visit(int i, int j) {   // i, j 행과열, 행열(row, column)
    static int pathCount = 1;   // static으로 전역 변수로서 사용
    // 찾은 경로의 개수를 저장
    Path_i[top] = i;
    Path_j[top++] = j;
    // top++;
    if(i == Ei && j == Ej) {  // 도착했을 때, 미로 탐색 성공
        printf("찾은 경로 %d번 \n", pathCount++);
        printPath();
        success = 1;
    }
    //미로 탐색 알고리즘
    if (m[i][j + 1] == 0)   //  배열의 오른쪽으로 이동
        visit(i, j + 1);
    if (m[i + 1][j] == 0)   // 배열 아래행으로 이동
        visit(i + 1, j);
    if(m[i][j - 1] == 0)    // 배열 왼쪽으로 이동
        visit(i, j - 1);
    if(m[i - 1][j] == 0)    // 배열 위로 이동
        visit(i - 1, j);
    top--;  // 모두 막혔다면 지난 경로로 다시 돌아가기 위해서!
    m[i][j] = 0;
    return success;
    
}
